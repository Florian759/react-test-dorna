export function getCircuitUrl(code: string): string {
	return `https://dornacorporatestorage.blob.core.windows.net/public-assets/images/events/${code}/circuit.png`;
}

export function getFlagUrl(code: string): string {
	return `https://dornacorporatestorage.blob.core.windows.net/public-assets/images/events/${code}/flag.png`;
}
